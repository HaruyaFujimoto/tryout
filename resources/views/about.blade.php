<h2>目的</h2>
<p>
ネット上にはたくさんの簡単にアクセスできるプログラミングの知識があり、またオンラインサロンのように仲間を見つける手段もあります。<br>
プログラミングとは命令の羅列であり、最終的に人に使ってもらうためにアプリケーションの形になる場合が多いと思います。<br>
このサイトは、
技術を学んだのだけれど、実際にどのような形のものを作るのかが空白である
という方へ向けて、アプリケーションの形の例を「ポートフォリオの企画」として見ることができるように作成しました。<br>
</p>
<p>
学んだ技術は使わなければ1週間後には忘れて、また検索することになることが多いと思います。<br>
実務に向けて、実践に近い成果物作成をして技術の活用を見に染み込ませたい方に「企画一覧」を活用していただけたらと思います。<br>
</p>
<p>
また、企画を詳細まで詰めるためのウェブアプリケーションも作成したいと思っています。
</p>
<h2>提供されるもの</h2>
<p>
企画の着想時点での「企画名」「目的・動機」「詳細」を見ることができます。<br>
企画の利用は自由で、自身で企画を投稿することもできます。<br>
また、まだ未実装ですが、企画に対して興味を持っているユーザーとその企画が紐づくようにしていますので、<br>
興味のあるユーザー同士で作成してみるのも楽しいかもしれません。
</p>

<h2>未実装、検討中の機能</h2>
<ul>
